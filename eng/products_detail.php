    const nav = document.querySelectorAll('.productsDetail .nav li');
    const check = document.querySelectorAll('.productsDetail .content');
    const options = {
      root: null,
      rootMargin: '0px 0px 0px 0px',
      threshold: 0.3,
    };
    const callback = (entries, observer) => {
      if (entries[0].isIntersecting) {
        entries[0].target.classList.add("active");
        nav.forEach(v => v.classList.remove("active"));
        check.forEach((v, i) => {
          if (v === entries[0].target) {
            nav[i].classList.add("active")
            navBox.scrollLeft = navScroll[i];
          }
        })
      }
    };

    const observer = new IntersectionObserver(callback, options);
    check.forEach(item => observer.observe(item))
