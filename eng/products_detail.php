      })

      $('.productsDetail .topInfo .infoBox .inMore').on('click', function() {
        $(this).toggleClass('active')
        if (!$('.productsDetail .topInfo .infoBox .description').is('.active')) {
          $('.productsDetail .topInfo .infoBox .description').addClass('active').css('height', maxHeight);
        } else {
          $('.productsDetail .topInfo .infoBox .description').removeClass('active').css('height', minHeight);
        }
      })

      $('.productsDetail .nav li').off().on('click', function() {
        $('.productsDetail .nav li').removeClass('active');
        $(this).addClass('active');
        let now = $('.content').eq($(this).index()).offset().top - $('.productsDetail .nav').height() - $('.siteHeader').height() - 20;
        window.scrollTo({
          top: now,
          behavior: 'smooth',
        });
      })


    })


    let navBox = document.querySelector('.productsDetail .nav');
